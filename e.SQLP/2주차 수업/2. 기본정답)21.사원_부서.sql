DROP INDEX YOON.IX_T_DEPT_LOC;
CREATE INDEX YOON.IX_T_DEPT_LOC ON YOON.T_DEPT(LOC);

DROP INDEX YOON.IX_T_EMP_02;
CREATE INDEX YOON.IX_T_EMP_02 ON YOON.T_EMP(DEPT_CODE, DIV_CODE);

